              <a href="https://postentryquarantine.govspace.gov.au/files/2015/05/IMG_7686-cropped.jpg"><img class="size-medium wp-image-669" src="https://postentryquarantine.govspace.gov.au/files/2015/05/IMG_7686-cropped-300x181.jpg" alt="Photo of concrete panels for dog kennels" width="300" height="181" srcset="http://postentryquarantine.govspace.gov.au/files/2015/05/IMG_7686-cropped-300x181.jpg 300w, http://postentryquarantine.govspace.gov.au/files/2015/05/IMG_7686-cropped-1024x620.jpg 1024w, http://postentryquarantine.govspace.gov.au/files/2015/05/IMG_7686-cropped-695x421.jpg 695w, http://postentryquarantine.govspace.gov.au/files/2015/05/IMG_7686-cropped-247x150.jpg 247w" sizes="(max-width: 300px) 100vw, 300px"/></a>
              <p class="wp-caption-text">A dog kennel run under construction at the new PEQ facility. The enclosures will include outdoor areas and indoor areas with underfloor heating.</p>
            </div>
            <p>Anyone who’s owned pets knows how Rover and Mittens quickly become part of the family. But what happens should you decide to move to Australia? Rather than leaving their much-loved pet behind, many families will seek to bring their cat or dog to Australia. Cats and dogs are also imported by breeders within Australia to improve and diversify genetic bloodlines.</p>
            <p>Owners can import cats and dogs to Australia from over 100 countries around the world. Before leaving their country of origin, cats and dogs must meet a series of import conditions including vaccinations, testing and treatment for a range of diseases and for internal and external parasites. Countries approved for import are classified into
              <a title="Step-by-step guide to preparing animals from countries in each category" href="http://www.agriculture.gov.au/cats-dogs/step-by-step-guides" target="_blank">three categories </a>according to disease risk, and these categories affect the conditions of entry and quarantine requirements for cats and dogs. Category one territories and countries have a health status similar to Australia, and subject to
              <a href="http://www.agriculture.gov.au/cats-dogs/step-by-step-guides/category-1-new-zealand" target="_blank">conditions</a>, cats and dogs from these countries can travel to Australia without a permit or being quarantined after arrival. Most approved countries are category two or three, which means that rabies is absent or well-controlled but animals still require quarantine upon arrival in Australia due to other disease risks. Cats and dogs from these countries must go to a government-operated Post Entry Quarantine facility for a minimum of 10 days. During this time they are monitored to further confirm their good health status. Post Entry Quarantine is the final step in the
              <a href="http://http://www.agriculture.gov.au/cats-dogs/step-by-step-guides" target="_blank">importing process</a> to prevent exotic diseases from threatening our domestic animals and human health.
            </p>
            <p>The new facilities for the Post Entry Quarantine of cats and dogs are currently being built to replace the existing facilities in both Sydney and Melbourne. The new facilities are being built in two stages and will provide capacity for the quarantine of 240 cats and 400 dogs when completed in 2018. The first stage is expected to be completed and operating in late 2015. From this time cats and dogs will need to enter Australia through Melbourne airport and complete their Post Entry Quarantine at the new facility before being transported to their new homes throughout Australia.</p>
            <p>Assistance dogs are subject to different conditions and will not be affected by the opening of the new Post Entry Quarantine Facility. For eligibility criteria and more information on bringing your assistance dog to Australia, click
              <a title="Assistance Dogs" href="http://www.agriculture.gov.au/cats-dogs/assistance-dogs">here</a>.
            </p>
            <p>For further information on bringing cats, dogs and other pets to Australia, click
              <a title="Bringing cats and dogs to Australia" href="http://www.agriculture.gov.au/cats-dogs">here</a>. For further information on the design of the cat and dog compounds, visit the
              <a title="Facility design" href="http://postentryquarantine.govspace.gov.au/design/">facility design</a> page.
            </p>
