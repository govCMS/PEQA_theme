          <header class="entry-header">
            <h1 class="entry-title">
              <a href="http://postentryquarantine.govspace.gov.au/2015/03/18/operational-commissioning-of-plant-compound-commenced/" rel="bookmark">Testing begins at plant compound</a>
            </h1>

            <div class="entry-meta">
              Posted on
              <a href="http://postentryquarantine.govspace.gov.au/2015/03/18/operational-commissioning-of-plant-compound-commenced/" title="2:58 pm" rel="bookmark">
                <time class="entry-date" datetime="2015-03-18T14:58:47+00:00">March 18, 2015</time>
              </a><span class="byline"> by <span class="author vcard"><a class="url fn n" href="http://postentryquarantine.govspace.gov.au/author/postentryquarantine/" title="View all posts by postentryquarantine" rel="author">postentryquarantine</a></span></span>
            </div><!-- .entry-meta -->
          </header><!-- .entry-header -->

          <div class="entry-content">
            <div id="attachment_591" style="width: 655px" class="wp-caption aligncenter">
              <a href="https://postentryquarantine.govspace.gov.au/files/2015/03/Pomegranate.jpg"><img class="wp-image-591 size-large" src="https://postentryquarantine.govspace.gov.au/files/2015/03/Pomegranate-1024x768.jpg" alt="Domestic pomegranate plants in one of the newly constructed greenhouses" width="645" height="483" srcset="http://postentryquarantine.govspace.gov.au/files/2015/03/Pomegranate-1024x768.jpg 1024w, http://postentryquarantine.govspace.gov.au/files/2015/03/Pomegranate-300x225.jpg 300w, http://postentryquarantine.govspace.gov.au/files/2015/03/Pomegranate-695x521.jpg 695w, http://postentryquarantine.govspace.gov.au/files/2015/03/Pomegranate-200x150.jpg 200w" sizes="(max-width: 645px) 100vw, 645px"/></a>
              <p class="wp-caption-text">Domestic pomegranates in one of the new greenhouses</p>
            </div>
            <p>More than 500 domestic plants were delivered to the new Post Entry Quarantine facility in Mickleham, Victoria last week. Over the next four months, these plants will be housed in various greenhouses within the plant compound while operational commissioning tests are progressing. Although construction of the laboratory within the plant compound is still underway, the plants have been brought in now to allow us to finetune functions in the greenhouses as early as possible. Testing our processes early, and across all Melbourne seasons, will help ensure a smooth transition of operations to the new facility.</p>
            <p>When complete, the new biosecure facility will include 2000 square metres of greenhouse space, which represents a 33% increase in capacity from current facilities. The new plant compound will also include an integrated diagnostics laboratory which will be used by our plant pathologists to monitor the health of the plants in quarantine.</p>
            <p>When the new facility opens at the end of 2015, this will be the only Commonwealth-operated facility for high-risk plants imported to Australia. As such, the compound will provide post entry quarantine services for plants suited to the broad range of conditions across the country. To mimic Australia’s different climatic regions, the greenhouses are divided into zones where factors such as temperature and humidity can be independently adjusted for each house. Maintaining appropriate growing conditions within each greenhouse during the quarantine period is required to ensure adequate plant growth for the disease screening process.</p>
            <p>Some commodity lines, including citrus plants, need to be held in quarantine for a minimum of two years to ensure that they are free of diseases that could harm our horticultural, agricultural and forestry industries, and our environment.</p>
            <p>This week, additional tropical plants such as bananas and lychees have also been moved into the greenhouses to test how the greenhouse functions to maintain warm, humid conditions during the Melbourne autumn and winter.</p>
            <p class="wp-caption-dd">For more information on how to import plants into Australia click
              <a title="How to import plants" href="http://http://www.agriculture.gov.au/import/plant-products/how-to-import-plants" target="_blank">here</a>.
            </p>
