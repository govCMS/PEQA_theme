          <div class="entry-content">
            <p>The second phase of the build of the new Post Entry Quarantine facility in Mickleham has started.</p>
            <p>Construction of the avian facility is now underway following the recent ground breaking at the site where the compound will be located. The Avian facility will be completed by 2018 and will replace current facilities at Torrens Island and Spotswood.</p>
            <p>The facility at Mickleham will combine live bird and hatching egg capabilities in a single building envelope. The envelope will contain a series of individual and completely separate containment areas, all with separate access.</p>
            <p>The combined floor space of the three hatching egg areas represents more than a three-fold increase in the Torrens Island capacity and is capable of further expansion.</p>
            <p>The capacity of the two live bird areas has also increased by almost 100 per cent with the total occupancy for pigeons raised to 300 in two separate areas. Individual areas are able to be used in isolation or occupied concurrently by one importer, depending on the size of the import consignment.</p>
            <p>The entire post entry avian quarantine compound has been designed to Quarantine Containment Level 3 to mitigate the biosecurity risks associated with avian imports. The design is complemented by operational procedures to protect each consignment from cross-contamination.</p>
            <p>Construction is also about to begin on the second stage of the cats and dogs facility and work on the ruminant facility is also starting.</p>
            <p>Operations for horses, cats, dogs and plants are have been underway at the new post entry quarantine (PEQ) facility since November 2015.</p>
