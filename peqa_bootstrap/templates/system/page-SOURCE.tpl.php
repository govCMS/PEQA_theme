        <div class="feature-container">
          <h2>Post entry quarantine in Australia </h2>
          <p>Biosecurity is about keeping Australia safe from exotic pests and diseases that could threaten our human, plant and animal health and our way of life.
            We work with industry, other government agencies and the community to reduce the risk of unwanted pests and diseases arriving or establishing in Australia.
            Our four government-operated post entry quarantine facilities manage high risk animal and plant imports.</p>

          <p>A new quarantine facility is being built to consolidate all post entry quarantine operations to better meet Australia’s future post entry quarantine needs. </p>
