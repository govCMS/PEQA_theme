          <div class="entry-content">
            <p>Australia is recognised worldwide as a producer of clean, green and disease and pest free agricultural products. This is reflected in the demand for Australian exports, valued at $41 billion in 2013–14. Australia’s strong biosecurity system and favourable biosecurity status supports Australian producers to access international markets. Assisting farmers to access premium markets is a priority for the Government, as outlined in the recently released Agricultural Competitiveness White paper.</p>
            <p>Australia’s post entry quarantine regulations, including the new post entry quarantine facility, located at Mickleham in Victoria, are a key component of Australia’s biosecurity system. Post entry quarantine is the final step in the safe import of live animals and plants into Australia. The new facility will consolidate government owned post entry quarantine sites into one modern facility.</p>
            <p>The new post entry quarantine facility will provide quarantine services for plants, cats, dogs, bees, horses, ruminants and avian species. It will strengthen Australia’s biosecurity system through early detection and prevention of the exotic diseases and pests that can have significant effects on productivity, profitability and market access of Australian farmers. The recently released Agricultural Competitiveness White Paper demonstrates the importance of a strong biosecurity system in maintaining Australia’s favourable pest, diseases and weed status.</p>
            <p>Outbreaks of significant pests and diseases such as Foot-and-mouth disease (FMD) and highly pathogenic avian influenza are estimated to have a significant impact on production, profitability and market access. Australia is currently free of FMD, which has helped maintain access to premium red meat markets. Without an effective biosecurity system to manage the risk of an outbreak of FMD, the annual profits of beef, dairy and sheep enterprises would be decreased by 8-12 per cent. Post entry quarantine reduces the risk for producers of an outbreak of an exotic disease or pest and as a result helps to maintain Australia’s biosecurity status and access to international markets.</p>
            <p>For further information on the
              <a href="http://agwhitepaper.agriculture.gov.au/">Agricultural Competitiveness White paper</a> please visit the website. To learn more about the need to safeguard Australia’s biosecurity, visit the
