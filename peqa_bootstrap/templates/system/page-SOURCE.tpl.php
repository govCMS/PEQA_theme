              <a href="http://postentryquarantine.govspace.gov.au/2015/10/29/official-opening-ceremony-of-australias-new-post-entry-quarantine-facility/" rel="bookmark">Official opening ceremony of Australia’s new post entry quarantine facility</a>
            </h1>

            <div class="entry-meta">
              Posted on
              <a href="http://postentryquarantine.govspace.gov.au/2015/10/29/official-opening-ceremony-of-australias-new-post-entry-quarantine-facility/" title="2:02 pm" rel="bookmark">
