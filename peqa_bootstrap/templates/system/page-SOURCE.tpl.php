            <p>Australia is recognised internationally as a producer of outstanding horses for a wide variety of disciplines and events. Australia attracts worldwide interest and investment in these events, including the Melbourne Cup. Horses are imported into Australia for breeding to diversify local genetic stock and to compete in sporting events including racing, endurance and dressage. </p>
