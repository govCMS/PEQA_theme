              <a href="http://postentryquarantine.govspace.gov.au/2015/07/17/post-entry-quarantine-supporting-agricultural-industries/" rel="bookmark">Post entry quarantine supporting agricultural industries</a>
            </h1>

            <div class="entry-meta">
              Posted on
              <a href="http://postentryquarantine.govspace.gov.au/2015/07/17/post-entry-quarantine-supporting-agricultural-industries/" title="11:28 am" rel="bookmark">
