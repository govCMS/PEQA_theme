          <div class="entry-content">
            <p>The new Mickleham post entry quarantine facility in Victoria has been officially opened by the Hon. Barnaby Joyce MP. The facility will be available to provide post entry quarantine for bees, cats, dogs, horses and plants this year.</p>
            <div id="attachment_710" style="width: 310px" class="wp-caption aligncenter">
              <a href="https://postentryquarantine.govspace.gov.au/files/2015/10/Opening-plaque.jpg"><img class="size-medium wp-image-710" src="https://postentryquarantine.govspace.gov.au/files/2015/10/Opening-plaque-300x251.jpg" alt="Official Opening Plaque, 26 October 2015" width="300" height="251" srcset="http://postentryquarantine.govspace.gov.au/files/2015/10/Opening-plaque-300x251.jpg 300w, http://postentryquarantine.govspace.gov.au/files/2015/10/Opening-plaque-1024x858.jpg 1024w, http://postentryquarantine.govspace.gov.au/files/2015/10/Opening-plaque-695x582.jpg 695w, http://postentryquarantine.govspace.gov.au/files/2015/10/Opening-plaque-179x150.jpg 179w" sizes="(max-width: 300px) 100vw, 300px"/></a>
              <p class="wp-caption-text">Official Opening Plaque</p></div>
            <p>The opening ceremony was attended by industry and officials involved with the consultation, development and construction of the facility. The opening event included the unveiling of a plaque and a tour for interested stakeholders.</p>
            <p>Set on 144 hectares on the northern outskirts of Melbourne, the new facility will cater for the import of birds (pigeons), fertile hatching eggs of poultry, bees, cats, dogs, horses, ruminants (mainly alpacas) and plants. The site is more than five times the size of the largest existing facility.</p>
            <p>The construction of the new facility has been planned in two main phases, the first phase which has now been officially opened includes the bee facility, plant compound, horse compounds and the first stage of the dog and cat compounds. These compounds will be operational this year.</p>
            <p>The second phase is the building of the avian and ruminant compounds, and stage two of the dog and cat compounds which will be open in 2018.<br/>
              It has been designed featuring modern laboratories, cat and dog kennels with high standards of animal welfare, technically sophisticated design for the avian and plant compounds, horse compounds that provide greater flexibility, a high level of security and public zones for visitors. Importantly, the facility also has space for future growth.
            </p>
            <p>The Mickleham post entry quarantine facility is an important element of Australia’s biosecurity system that will support the management of high risk plant and animal imports into Australia.</p>
            <p>Preparations are well underway for the first arrival of dogs and cats in November.</p>
